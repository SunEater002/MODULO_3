// If solo
let a = 5
let b = 10

if (a < b) {
    console.log("a es menor que b")
    console.log("a sigue siendo menor que b")
}

if (a > b) {
    console.log("a es mayor que b")
    console.log("a sigue siendo mayor que b")
}

// If else
a = 5
b = 5

if (a < b) {
    console.log("a es menor que b")
} else {
    console.log("a es mayor que b")
}

// If else if
if (a < b) {
    console.log("a es menor que b")
} else if (a == b) {
    console.log("a es igual que b")
} else {
    console.log("a es mayor que b")
}